    //leetcode submit region begin(Prohibit modification and deletion)
    class Solution {
        public int[] numberOfLines(int[] widths, String S) {
            int[] res = new int[]{1, 0};
            char[] chars = S.toCharArray();
            int count = 0;
            for (char c : chars) {
                count += widths[c - 'a'];
                if (count > 100) {
                    // å½“æ”¾ä¸ä¸‹æ—¶,æ”¾å…¥ä¸‹ä¸€è¡Œ
                    res[0]++;
                    count = widths[c - 'a'];
                } else if (count == 100) {
                    // åˆšå¥½æ”¾å¾—ä¸‹
                    count = 0;
                    res[0]++;
                }
            }
            // è®°å½•æœ€åä¸€è¡Œçš„ä¸ªæ•°
            res[1] = count;
            return res;
        }
    }
